package org.globus.GenericPortal.common;

import java.util.*;
import java.io.*;
import java.lang.*;
import org.globus.GenericPortal.common.*;

public class CacheResp
{
    public String value;
    public String evictedKey;
    public boolean success = false;
    public int exitCode = 0;

    public CacheResp()
    {
        this.value = null;
        this.evictedKey = null;
        this.success = false;
        this.exitCode = 0;
    }

    public CacheResp(String v, String e)
    {
        this.value = v;
        this.evictedKey = e;
    }

}
